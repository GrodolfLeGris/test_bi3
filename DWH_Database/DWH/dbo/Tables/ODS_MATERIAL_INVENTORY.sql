CREATE TABLE [dbo].[ODS_MATERIAL_INVENTORY]
(	
	[ORGANIZATIONLVL3ID] INT NOT NULL , 
    [TRNDATE] DATE NOT NULL, 
    [MATERIAL_ID] INT NOT NULL, 
    [BUILDING_ID] INT NULL, 
    [LOCATION] NVARCHAR(80) NOT NULL, 
    [MATERIAL_STATUS] NVARCHAR(50) NOT NULL, 
    [CONSIGMENT] BIT NULL, 
    [OPERATION_ID] INT NOT NULL, 
    [QUANTITY] DECIMAL(18, 6) NULL, 
    [UNIT_COST] DECIMAL(18, 6) NULL, 
    [EXT_COST] DECIMAL(18, 6) NULL, 
    [Manufacturer_Part_No] VARCHAR(100) NULL, 
    PRIMARY KEY ([ORGANIZATIONLVL3ID], [TRNDATE], [MATERIAL_ID], [LOCATION], [MATERIAL_STATUS], [OPERATION_ID])
)
